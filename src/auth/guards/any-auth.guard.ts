import { Injectable, ExecutionContext } from '@nestjs/common';
import { AuthGuard } from '@nestjs/passport';
import { Reflector } from '@nestjs/core';
import { IS_PUBLIC_KEY } from '../../common/decorators/public.decorator';

/**
 * Guard que acepta autenticación tanto por JWT (usuarios Google)
 * como por LDAP (dashboard administración).
 *
 * Se comporta como los demás guards respetando el decorador @Public().
 */
@Injectable()
export class AnyAuthGuard extends AuthGuard(['jwt', 'ldap']) {
  constructor(private reflector: Reflector) {
    super();
  }

  canActivate(context: ExecutionContext) {
    const isPublic = this.reflector.getAllAndOverride<boolean>(IS_PUBLIC_KEY, [
      context.getHandler(),
      context.getClass(),
    ]);

    if (isPublic) {
      return true;
    }

    return super.canActivate(context);
  }
}


